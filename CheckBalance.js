const tokenAddresses = {
  USDC: process.env.SC_USDC,
  USDT: process.env.SC_USDT,
  WBTC: process.env.SC_WBTC,
};

async function checkTokens() {
  for (const address of addresses) {
    // Convert the balance from Wei to Ether
    const balanceETH = await provider.getBalance(address.address);
    const etherBalance = ethers.utils.formatEther(balanceETH);

    console.log(`Checking tokens for address: ${address.address}`);
    console.log(`Balance: ${etherBalance}`);
    console.log("--------------------------");
    for (const tokenSymbol in tokenABIs) {
      const tokenContract = new ethers.Contract(
        tokenAddresses[tokenSymbol],
        tokenABIs[tokenSymbol],
        provider
      );

      try {
        const balance = await tokenContract.balanceOf(address.address);
        const decimals = await tokenContract.decimals();
        const symbol = await tokenContract.symbol();

        console.log(`Token: ${symbol} (${tokenSymbol})`);
        console.log(`Balance: ${ethers.utils.formatUnits(balance, decimals)}`);
        console.log("--------------------------");
      } catch (error) {
        console.log(`Error checking token: ${tokenSymbol}`, error);
      }
    }
  }
}
